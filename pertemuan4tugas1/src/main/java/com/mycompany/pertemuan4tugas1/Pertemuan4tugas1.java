public class Pertemuan4tugas1 {

    public static void main(String[] args) {
       int angka1=10;
       int angka2=3;
       System.out.println("A + B: " +
               (angka1 + angka2) );
       System.out.println("A - B: " +
               (angka1 - angka2) );
       System.out.println("A * B: " +
               (angka1 * angka2) );
       System.out.println("A / B: " +
               (angka1 / angka2) );
       System.out.println("A % B: " +
               (angka1 % angka2) );
    }
}